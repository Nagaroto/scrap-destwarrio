import os
import platform

print("Detectando sistema...")

sistema = platform.system()

if sistema == "Windows":
    print("Sistema Windows detectado")
    os.system("instala_windows.bat")

elif sistema == "Linux" or sistema == "Darwin":
    print("Sistema Linux/Mac detectado")
    os.system("chmod +x instala_linux.sh")
    os.system("./instala_linux.sh")

else:
    print("Sistema não suportado:", sistema)
